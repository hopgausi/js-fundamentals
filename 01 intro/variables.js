// strings
let name = "Hopson Gausi";
console.log(name, 'type: ' + typeof name);

// numbers
let age = 21;
let amount = 55000000.99;
console.log(age, 'type: ' + typeof age);
console.log(amount, 'type: ' + typeof amount);

// boolean
let isActive = true;
let isSuperUser = false;
console.log(isActive, 'type: ' + typeof isActive);
console.log(isSuperUser, 'type: ' + typeof isSuperUser);

// undefine - there's nothing inside the variable
let empty = undefined;
console.log(empty, 'type: ' + typeof empty);

// const keyword - for things that are constant: cannot be reassigned
const PI = 3.142857142857143;
console.log(PI, 'type: ' + typeof PI)

// objects
const profile = {
    name: 'Hopson Gausi',
    age: 24,
    contact: 994027141
}
console.log(profile, 'type: ' + typeof profile);